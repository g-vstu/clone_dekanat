package com.company.mysql;


public class Docenka {

  private long docenkaId;
  private String vidoc;
  private long ball;
  private long npp;
  private String ocenkaSim;
  private long ocenka;


  public long getDocenkaId() {
    return docenkaId;
  }

  public void setDocenkaId(long docenkaId) {
    this.docenkaId = docenkaId;
  }


  public String getVidoc() {
    return vidoc;
  }

  public void setVidoc(String vidoc) {
    this.vidoc = vidoc;
  }


  public long getBall() {
    return ball;
  }

  public void setBall(long ball) {
    this.ball = ball;
  }


  public long getNpp() {
    return npp;
  }

  public void setNpp(long npp) {
    this.npp = npp;
  }


  public String getOcenkaSim() {
    return ocenkaSim;
  }

  public void setOcenkaSim(String ocenkaSim) {
    this.ocenkaSim = ocenkaSim;
  }


  public long getOcenka() {
    return ocenka;
  }

  public void setOcenka(long ocenka) {
    this.ocenka = ocenka;
  }

}
